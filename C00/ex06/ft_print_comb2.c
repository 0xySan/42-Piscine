#include <unistd.h>

void	ft_print_comb2(void)
{
	int		i;
	int		j;
	char	a;
	char	b;

	i = -1;
	while (++i <= 98)
	{
		j = i;
		while (++j <= 99)
		{
			a = i / 10 + '0';
			b = i % 10 + '0';
			write(1, &a, 1);
			write(1, &b, 1);
			write(1, " ", 1);
			a = j / 10 + '0';
			b = j % 10 + '0';
			write(1, &a, 1);
			write(1, &b, 1);
			if (i != 98)
				write(1, ", ", 2);
		}
	}
}
